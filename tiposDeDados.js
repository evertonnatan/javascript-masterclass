// Os primitivos são imutáveis, ou seja, 
// ao longo do tempo seu valor não é alterado

// number
typeof 10;
// string
typeof "JavaScript";
// boolean
typeof true;
// symbol
typeof Symbol("iterator");
// null (é um objeto)
typeof null;
// undefined
typeof undefined;

// Cuidado! Não confunda os operadores "typeof" e "instanceof"